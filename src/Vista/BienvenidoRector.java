    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        //Generar PDF
        Document documento = new Document();
        try {
            String ruta = System.getProperty("user.home");
            PdfWriter.getInstance(documento, new FileOutputStream(ruta + "/Desktop/reporte.pdf"));
            documento.open();

            PdfPTable tabla = new PdfPTable(7);
            tabla.addCell("Codigo");
            tabla.addCell("Nota");
            tabla.addCell("Descripcion");
            tabla.addCell("Fecha");
            tabla.addCell("Matricula");
            tabla.addCell("Nombre"); //Ni√±o
            tabla.addCell("Grado");
            try {
                String sql = "select a.codigo, a.nota, a.descripcion, a.fecha, a.ni√±o as matriculaNi√±o, n.nombre, g.nombre\n"
                        + "from agenda a inner join ni√±os n on a.ni√±o = n.num_matricula inner join grado g on a.grado = g.codigo";
                Conexion conexion = new Conexion();
                PreparedStatement pst = conexion.conectar().prepareStatement(sql);
                ResultSet rs = pst.executeQuery();
                if (rs.next()) {
                    while (rs.next()) {
                        tabla.addCell(rs.getString(1));
                        tabla.addCell(rs.getString(2));
                        tabla.addCell(rs.getString(3));
                        tabla.addCell(rs.getString(4));
                        tabla.addCell(rs.getString(5));
                        tabla.addCell(rs.getString(6));
                        tabla.addCell(rs.getString(7));
                    }
                    documento.add(tabla);
                }
            } catch (DocumentException | SQLException e) {

            }
            documento.close();
            JOptionPane.showMessageDialog(null, "El reporte fue creado");
        } catch (DocumentException | HeadlessException | FileNotFoundException e) {

        }
    }//GEN-LAST:event_jButton2ActionPerformed
